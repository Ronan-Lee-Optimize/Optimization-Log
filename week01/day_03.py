# repeat from 0 to 4
for i in range (5):
    print("day", i)


# range(start, end): repeat from 1 to 5 (end not included)
for i in range (1,6):
    print("day", i)


# loop though a list one by one
subjects = ["math", "physics", "literature"]

for subject in subjects:
    print("I'm studying",subject)


